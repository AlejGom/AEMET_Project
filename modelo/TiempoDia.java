package modelo;

public class TiempoDia {

	//Atributos
	private String fecha;
	private String estadoCielo;
	private String tempMax;
	private String tempMin;
	private String sensMax;
	private String sensMin;
	private String velViento;
	private String dirViento;
	private String uvMax;
	
	//Getters and Setters
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	public String getEstadoCielo() {
		return estadoCielo;
	}
	public void setEstadoCielo(String estadoCielo) {
		this.estadoCielo = estadoCielo;
	}
	public String getTempMax() {
		return tempMax;
	}
	public void setTempMax(String tempMax) {
		this.tempMax = tempMax;
	}
	public String getTempMin() {
		return tempMin;
	}
	public void setTempMin(String tempMin) {
		this.tempMin = tempMin;
	}
	public String getSensMax() {
		return sensMax;
	}
	public void setSensMax(String sensMax) {
		this.sensMax = sensMax;
	}
	public String getSensMin() {
		return sensMin;
	}
	public void setSensMin(String sensMin) {
		this.sensMin = sensMin;
	}
	public String getVelViento() {
		return velViento;
	}
	public void setVelViento(String velViento) {
		this.velViento = velViento;
	}
	public String getDirViento() {
		return dirViento;
	}
	public void setDirViento(String dirViento) {
		this.dirViento = dirViento;
	}
	public String getUvMax() {
		return uvMax;
	}
	public void setUvMax(String uvMax) {
		this.uvMax = uvMax;
	}

}
